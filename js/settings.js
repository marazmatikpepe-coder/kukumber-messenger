// Автоопределение языка при загрузке (если не выбран)
function detectUserLanguage() {
    var savedLang = localStorage.getItem('kukumber_language');
    if (savedLang && translations[savedLang]) {
        currentLanguage = savedLang;
    } else {
        var browserLang = navigator.language || navigator.userLanguage;
        browserLang = browserLang.substring(0, 2).toLowerCase();
        var supported = ['ru', 'en', 'es', 'de', 'it', 'fr', 'be', 'uk', 'pl', 'ko', 'nl', 'pt_br', 'ca', 'ms', 'tr', 'fa', 'uz', 'ar', 'id', 'hu', 'iw', 'hi', 'kk', 'zh'];
        if (supported.indexOf(browserLang) !== -1 && translations[browserLang]) {
            currentLanguage = browserLang;
        } else {
            currentLanguage = 'ru';
        }
        localStorage.setItem('kukumber_language', currentLanguage);
    }
    applyTranslations();
}

